import numpy as np
import pandas as pd
import seaborn as sns
from matplotlib import pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn import svm
from sklearn.metrics import accuracy_score

dataset=pd.read_csv('/content/train_u6lujuX_CVtuZ9i (1).csv')
dataset.head()

dataset.shape

dataset.describe()

dataset.info()

dataset.isnull().sum()

#drop all the missing values
dataset=dataset.dropna()

dataset.isnull().sum()

dataset.replace({'Married':{'No':0,'Yes':1},'Gender':{'Male':1,'Female':0}, 'Education':{'Graduate':1, 'Not Graduate':0}}, inplace=True)

dataset.head()

dataset.replace({'Loan_Status': {'N':0, 'Y':1}}, inplace=True)

dataset.head()

#dependent column values
dataset['Dependents'].value_counts()

#replace the value of 3+ to 4
dataset=dataset.replace(to_replace='3+', value=4)

dataset['Dependents'].value_counts()

#data visualisation
sns.countplot(x='Education', hue='Loan_Status', data=dataset)

#marital status and loan status
sns.countplot(x='Married', hue='Loan_Status', data=dataset)

dataset.replace({'Property_Area':{'Rural':0, 'Semiurban':1, 'Urban':2}}, inplace=True)

dataset.replace({'Self_Employed':{'Yes':1, 'No':0}}, inplace=True)

X=dataset.drop(columns=['Loan_ID', 'Loan_Status'], axis=1)
Y=dataset['Loan_Status']

print(X)

print(Y)

X_train, X_test, Y_train, Y_test = train_test_split(X,Y, test_size=0.1, stratify=Y, random_state=2)
print(X_train.shape)
print(X_test.shape)
print(Y_train.shape)
print(Y_test.shape)

#model traing with support vector machine
classifier=svm.SVC(kernel='linear')

#traing the support vector machine
classifier.fit(X_train, Y_train)

#accuracy score on training data
X_train_prediction=classifier.predict(X_train)
training_data_accuracy=accuracy_score(X_train_prediction, Y_train)

print('Accuracy on training data: ', training_data_accuracy)

#accuracy on test data
X_test_prediction=classifier.predict(X_test)
test_data_accuracy=accuracy_score(X_test_prediction, Y_test)

print('Accuracy on test data: ', test_data_accuracy)

